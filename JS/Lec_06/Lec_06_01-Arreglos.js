// Un arreglo es un tipo object que admite el almacenamiento de múltuples valores.
/**
 * Hay dos maneras de definir un arreglo en Javascript, veamos cuáles son:
 * 
 * Manera #1:
 * let array = new Array('val1','val2',...)
 * 
 * Manera #2:
 * const array = ['val1','val2',...]
 * */

let array1 = new Array('val1','val2')
console.log(array1)

const array2 = ['val1','val2']
console.log(array2)
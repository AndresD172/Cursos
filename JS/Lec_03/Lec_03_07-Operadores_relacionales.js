/* Los operadores relacionales nos permiten, como dice su nombre, establecer relación entre 2 valores dados. Estos opera-
dores son los mismos a los operadores de comparación en las matemáticas (< (menor que), > (mayor que) e == (igual a)), ade-
más, a la lista de operadores relacionales de toda la vida, se les añaden los operadores relacionales de <= (menor o igual 
a) y >= (mayor o igual a). */

let a = 1, b = 3;

// Operadores de comparación mayor que y mayor o igual que.

let v = b > a;

console.log(v)

let w = b >= a;

console.log(w);

// Operadores de menor que y menor o igual que.

let x = a < b;

console.log(x);

let y = a <= b;

console.log(y);

// Operador de igual que.

let z = a == b;

console.log(z);

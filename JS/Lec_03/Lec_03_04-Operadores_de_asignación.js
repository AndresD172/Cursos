/* En JavaScript también contamos con los operadores de asignación, al igual que en Python. Los operadores de asignación
nos ayudan a asignar un valor a una variable. Estos son: = (operador de asignación básico), += (suma en asignación), -= (
resta en asignación), *= (multiplicación en asignación), /* (división en asignación), %= (módulo en asignación) y **= (ex-
ponente en asignación). */

/* La función que realizan estos operadores es la de realizar una operación y guardar el resultado en la misma variable. Por
ejemplo: */

let a = 1;

a += 1;

console.log(a);

// Modo de operación: a = a + 1

// De la misma manera funcionan el resto de los operadores.
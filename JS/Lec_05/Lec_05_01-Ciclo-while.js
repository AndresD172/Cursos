/**
 * El ciclo while se encarga de ejecutar un bloque de código dependiendo del valor de la condición. Siempre que dicha condición
 * sea verdadera el código dentro de la sentencia se ejecutará; cuando esta sea falsa, el bucle se detendrá. 
*/

let valCondicional = 1;

while (valCondicional <= 10) {
  console.log(valCondicional)
  valCondicional++
}